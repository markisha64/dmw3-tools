#[derive(Copy, Clone, Debug, PartialEq, Eq)]
pub enum Moves {
    Named(NamedMoves),
    Unnamed(usize),
}

impl From<usize> for Moves {
    fn from(value: usize) -> Self {
        match NamedMoves::try_from(value) {
            Ok(mv) => Moves::Named(mv),
            Err(_) => Moves::Unnamed(value),
        }
    }
}

impl From<&str> for Moves {
    fn from(value: &str) -> Self {
        Moves::Named(NamedMoves::from(value))
    }
}

impl From<Moves> for usize {
    fn from(value: Moves) -> Self {
        match value {
            Moves::Named(mv) => mv as usize,
            Moves::Unnamed(n) => n,
        }
    }
}

impl From<Moves> for &str {
    fn from(value: Moves) -> Self {
        match value {
            Moves::Named(mv) => mv.into(),
            Moves::Unnamed(_) => "",
        }
    }
}

